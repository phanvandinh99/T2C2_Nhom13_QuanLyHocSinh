package com.example.demo3_asm;

public class ModalLopHoc {
    public String MaLopHoc;
    public String TenLopHoc;
    public ModalLopHoc() {

    }

    public ModalLopHoc(String maLopHoc, String tenLopHoc) {
        MaLopHoc = maLopHoc;
        TenLopHoc = tenLopHoc;
    }

    public String getMaLopHoc() {
        return MaLopHoc;
    }

    public void setMaLopHoc(String maLopHoc) {
        MaLopHoc = maLopHoc;
    }

    public String getTenLopHoc() {
        return TenLopHoc;
    }

    public void setTenLopHoc(String tenLopHoc) {
        TenLopHoc = tenLopHoc;
    }
}
