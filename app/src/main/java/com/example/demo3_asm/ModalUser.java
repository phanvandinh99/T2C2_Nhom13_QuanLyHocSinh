package com.example.demo3_asm;

public class ModalUser {
    public String User;
    public String Passs;

    public ModalUser(String user, String passs) {
        User = user;
        Passs = passs;
    }

    public String getUser() {
        return User;
    }

    public void setUser(String user) {
        User = user;
    }

    public String getPasss() {
        return Passs;
    }

    public void setPasss(String passs) {
        Passs = passs;
    }
}
